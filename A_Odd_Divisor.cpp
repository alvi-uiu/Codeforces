inline void solve()
{
    ll n;
    cin >> n;

    if (n % 2 != 0)
    {
        cout << "YES" << "\n";
        return;
    }
    else
    {
        while (n % 2 == 0)
        {
            n /= 2;
        }
        if (n > 1)
            cout << "YES" << "\n";

        else
            cout << "NO" << "\n";
    }
}

int main()
{
    FAST_IO;

    int t;
    cin >> t;
    while (t--)
    {
        solve();
    }

    return 0;
}

