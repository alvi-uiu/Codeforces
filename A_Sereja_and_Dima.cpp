#include <bits/stdc++.h>
using namespace std;

#define fastio                        \
    ios_base::sync_with_stdio(false); \
    cin.tie(NULL);                    \
    cout.tie(NULL)

int main()
{
    fastio;

    int n;
    cin >> n;

    vector<int> arr(n);
    for (int i = 0; i < n; i++)
        cin >> arr[i];

    int sereja = 0, dima = 0, left = 0, right = n - 1, cnt = 2;

    while (left <= right)
    {
        if (arr[left] > arr[right])
        {
            if (cnt % 2 == 0)
                sereja += arr[left];
            else
                dima += arr[left];

            left++;
        }
        else
        {
            if (cnt % 2 == 0)
                sereja += arr[right];
            else
                dima += arr[right];

            right--;
        }
        cnt++;
    }

    cout << sereja << " " << dima << "\n";

    return 0;
}
